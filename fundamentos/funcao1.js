//função sem retorno
function soma(a=0, b=0){
    console.log(a+b)
}
function subtracao(a, b){
    console.log(a-b)
}
soma(2+2)
subtracao(9, 7)

// funcao com retorno
function multiplicacao(a, b){
    return a*b
}

function divisao(a, b){
    return a/b
}
console.log(multiplicacao(2, 10))
console.log(divisao(10,2))
